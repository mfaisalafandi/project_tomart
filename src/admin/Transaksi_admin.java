    private void autonumber(){
        try {
            Connection c = koneksi.configDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tb_transaksi ORDER BY noTransaksi DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("noTransaksi").substring(2);
                String TR = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                switch (TR.length()) {
                    case 1:
                        Nol = "000";
                        break;
                    case 2:
                        Nol = "00";
                        break;
                    case 3:
                        Nol = "0";
                        break;
                    case 4:
                        Nol = "";
                        break;
                    default:
                        break;
                }
                noTrx.setText("TR" + Nol + TR);
            } else {
                noTrx.setText("TR0001");
            }
            r.close();
            s.close();
        } catch (NumberFormatException | SQLException e) {
            System.out.println("autonumber error");
        }
    }
    
