    private void save(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        String noTransaksi = noTrx.getText();
        String tanggal = trxDate.getText();
        int total = Integer.parseInt(totalTrx.getText());

        try {
            Connection c = koneksi.configDB();
            String sql = "INSERT INTO tb_transaksi VALUES (?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, noTransaksi);
            p.setString(2, tanggal);
            p.setInt(3, total);
            p.setInt(4, 0); // 0 = pending, 1 = dibayar
            p.execute();
            p.close();
        } catch (SQLException e) {
            System.out.println("simpan penjualan error" + e);
        }
        
        try {
            Connection c = koneksi.configDB();
            int baris = jTable1.getRowCount();
            
            for (int i = 0; i < baris; i++) {
                System.out.println(noTransaksi + "," + jTable1.getValueAt(i, 1));
                String sql = "INSERT INTO tb_detail_transaksi (noTransaksi, idProduk, namaBarang, qty, harga, total) VALUES('"
                        + noTransaksi +"','"+ jTable1.getValueAt(i, 1) +"','"+ jTable1.getValueAt(i, 2) 
                        +"','"+ jTable1.getValueAt(i, 3) +"','"+ jTable1.getValueAt(i, 4) +"','"+ jTable1.getValueAt(i, 5) 
                        +"')";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (SQLException e) {
            System.out.println("simpan detail penjualan error" + e);
        }
        clearBarang();
        clearListBarang();
        clearMenuUtama();
        autonumber();
        jLabel9.setText("Rp. 0");
        JOptionPane.showMessageDialog(null, "Penjualan berhasil disimpan");
    }
